mySqr = [x^2 | x <- [1..10]]
-- => [1, 4, 9, 16, 25, 36, 49, 64, 81, 100]

a = [x | x <- mySqr, rem x 2 == 0]
-- => [4, 16, 36, 64, 100]

b = [(x, y) | x <- mySqr, y <- mySqr, x < 50, y > 50]
-- => [
-- (1, 64), (1, 81), (1, 100),
-- (4, 64), (4, 81), (4, 100),
-- ...
-- (49, 64), (49, 81), (49, 100)
-- ]

c = take 5 [ (x, y) | x <- mySqr, y <- mySqr, x < 50, y > 50 ]
-- => [(1, 64), (1, 81), (1, 100), (4, 64), (4, 81)]
